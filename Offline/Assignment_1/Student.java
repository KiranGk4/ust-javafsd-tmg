class Student
{
    private String name;
    private int age, rollNo;
    public Student(String name, int age, int rollNo) {
        this.name = name;
        this.age = age;
        this.rollNo = rollNo;
    }

    public int getAge() {
        return age;
    }

    public int getRollNo() {
        return rollNo;
    }



    public String getName() {
        return name;
    }


}
